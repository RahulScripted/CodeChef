//Write a program in C++ to display various type or arithmetic operation using mixed data type
#include<iostream>
using namespace std;
int main(){
    cout<<"Sum will be : "<<(444+89)<<endl;
    cout<<"Sum will be : "<<(4.44+8.9)<<endl;
    cout<<"Sum will be : "<<(-444-89)<<endl;
    cout<<"Subtraction will be : "<<(-56+30)<<endl;
    cout<<"Subtraction will be : "<<(56-30)<<endl;
    cout<<"Subtraction will be : "<<(42.44-8.9)<<endl;
    cout<<"Multiplication will be : "<<(16 * 81)<<endl;
    cout<<"Multiplication will be : "<<(13.6 * 8.1)<<endl;
    cout<<"Multiplication will be : "<<((-16.9) * 81)<<endl;
    cout<<"Integer division will be : "<<(32/5)<<endl;
    cout<<"Remainder will be : "<<(64%3)<<endl;
}