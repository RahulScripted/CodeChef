// Sometimes - we have to accept multiple inputs in a single line. We can use the same syntax to accept multiple inputs in a single line, however the different inputs will be separated by a '>>' operator.



#include <bits/stdc++.h>
using namespace std;

int main(){
    int A, B, C;
    cin >> A >> B >> C;
    cout << A << " " << B << " " << C;
    return 0;
}