// Write a program which does the following: Find the area of a circle whose radius is 8.9 cm. Take pi = 3.14




#include <bits/stdc++.h>
using namespace std;

int main() {
    double pi = 3.14;
    double radius = 8.9; 
    double area = pi * radius * radius;
    cout << "The Area of the given Circle is ";
    cout << area;
}