// Write a program which does the following:

// 1.  Initialise a int variable n and accept a user defined input from the console.

// 2.  Output "C++" to the console n number of times.




#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    cin >> n;
    for (int i = 0; i < n; i++) cout << "C++" << endl;
}