// Write a program which does the following:

// 1.   Find the area of a circle whose radius is 8.9. Take pi = 3.14

// 2.   Declare variables radius, pi and area and assign the relevant values to them

// 3.   Formula for area of a circle is Pi * radius * radius



#include <iostream>
using namespace std;

int main() {
    double pi = 3.14;
    double radius = 8.9;
    double area = (pi * radius * radius);
    
    cout << "The Area of the given Circle is " << area << endl;

}