// Write a program which does the following:

// 1.  Variable a and assign it the value - 50

// 2.  Variable b and assign it the value 40

// 3.  Variables sum, product and quotient - assign the relevant values to them

// Output sum, product and quotient to the console on separate lines




#include <bits/stdc++.h>
using namespace std;

int main() {
    int a = -50;
    int b = 40;
    int sum = (a + b);
    int product  = (a * b);
    int quotient   = (a / b);
    
    cout<<sum<<endl;
    cout<<product<<endl;
    cout<<quotient<<endl;
}