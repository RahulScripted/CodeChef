// Following operations: sqrt(n), __gcd(a,b), floor(n), ceil(n),pow(x,y)



#include <bits/stdc++.h>
using namespace std;

int main() {
    double n = 26;
    cout << sqrt(n) << endl;

    int a = 16;
    int b = 24;
    cout << __gcd(a,b) << endl;
    
    double f = 1.89;
    cout << floor(f) << endl;
    
    n = 0.21;
    cout << ceil(n) << endl;
    
    int x = 6;
    int y = 3;
    cout << pow(x,y) << endl;
}